constexpr int N = 1e5 + 10;
int n, res;
string s;

void dfs(unordered_set<string> mp, string t, int cur) {
    //<string, int> mp, string t, int cur) {
    if (cur == n) {
        chmax(res, SZ(mp));
    } else {
        if (cur == n - 1) {
            t += s[cur];
            if (mp.find(t) == mp.end()) {
                chmax(res, SZ(mp) + 1);
            }
        } else {
            if (SZ(mp) + n - cur < res)
                return;
            t += s[cur];
            dfs(mp, t, cur + 1);
            if (mp.find(t) == mp.end()) {
                mp.insert(t);
                dfs(mp, "", cur + 1);
            }
        }
    }
}

