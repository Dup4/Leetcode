class Solution {
public:
    int minNumberOfFrogs(string croakOfFrogs) {
        s = croakOfFrogs;
        int cnt[10] = {0};
        int res = 0;
        mp['c'] = 0;
        mp['r'] = 1;
        mp['o'] = 2;
        mp['a'] = 3;
        mp['k'] = 4;
        for (auto &c : s) {
            if (c == 'c')
                ++cnt[0];
            else {
                if (cnt[mp[c] - 1] == 0)
                    return -1;
                --cnt[mp[c] - 1];
                ++cnt[mp[c]];
            }
            int sum = cnt[0] + cnt[1] + cnt[2] + cnt[3];
            chmax(res, sum);
        }
        return res;
    }
};

