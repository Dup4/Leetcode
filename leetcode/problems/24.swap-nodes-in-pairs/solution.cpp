struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

#endif

class Solution {
public:
    ListNode *swapPairs(ListNode *head) {
        if (head == nullptr) {
            return head;
        }

        ListNode *pre = nullptr;
        auto *tmp = head;
        int ix = 0;
        while (tmp) {
            if ((ix & 1) && pre) {
                swap(pre->val, tmp->val);
            }

            pre = tmp;
            tmp = tmp->next;

            ++ix;
        }

        return head;
    }
};

