class DataStream {
public:
    int value{0};
    int k{0};
    int has{0};
    queue<int> q;

    DataStream(int value, int k) {
        this->value = value;
        this->k = k;
        this->has = 0;
    }

    bool consec(int num) {
        if (num == value) {
            ++has;
        }

        q.push(num);
        while (q.size() > size_t(k)) {
            int x = q.front();
            q.pop();

            if (x == value) {
                --has;
            }
        }

        return has == k;
    }
};

